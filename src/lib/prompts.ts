/**
 * Generate a system prompt for an agent from the agents catalog.
 */
export function getAgentPrompt(agent: { name: string; nameEn: string; desc: string; expertise: string; whenToUse: string }): string {
  const cliInfo = cliCapabilitiesPrompt()
  return `你是「${agent.name}」（${agent.nameEn}），${agent.desc}。

## 你的专业领域
${agent.expertise}

## 何时需要你
${agent.whenToUse}

## 工作原则
- 始终用中文回答（除非用户要求其他语言）
- 结合你的专业知识提供深度分析和实用建议
- 复杂内容用 Markdown 格式组织（标题、列表、表格等）
- 如需更多信息，主动询问用户
- 回答要具体且可操作，避免空泛建议
- 在你的专业领域内深入展开，给出专业人士级别的分析

## 工具能力
你拥有完整的 agent 工具链。当用户的需求涉及代码、文件或系统操作时，**主动使用工具**：
- **FileRead / FileWrite / FileEdit** — 读写和编辑本地文件
- **Glob / Grep** — 搜索文件和内容
- **Bash** — 执行终端命令（安装依赖、运行脚本、git 操作等）
- **WebFetch / WebSearch** — 搜索网络或抓取网页内容
- **Agent** — 派遣子代理处理子任务
- **TodoWrite** — 跟踪多步任务进度
- **EnterPlanMode / ExitPlanMode** — 复杂任务先做计划再执行

当用户要求你帮忙写代码、修改文件、查看项目结构、运行命令时，直接调用对应工具执行，不要仅给出建议。
${cliInfo}${COMMON_TOOL_NOTE}`
}

/**
 * Prepend a project-context block (typically CLAUDE.md content) to the feature
 * system prompt. When the user has set `projectContext` in settings, the model
 * sees it before the feature prompt.
 */
export function composeSystemPrompt(featurePrompt: string, projectContext?: string): string {
  const ctx = (projectContext || '').trim()
  const cliInfo = cliCapabilitiesPrompt()
  const capInfo = getCapabilityPrompt()
  const parts: string[] = []

  if (ctx) {
    parts.push(`<project-context>
Codebase / project instructions provided by the user. Adhere to these.

${ctx}
</project-context>`)
  }

  parts.push(featurePrompt)

  if (cliInfo) parts.push(cliInfo)
  if (capInfo) parts.push(capInfo)

  return parts.join('\n\n')
}
